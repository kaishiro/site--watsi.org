        <div class="chapter">

          <h3 class="chapter__heading">Treatments</h3>

          <div class="chapter__body">

            <section class="verse">
              <h3 class="verse__heading" id="about">Who is eligible for Watsi funding?</h3>
              <p class="verse__copy">
              In order to be eligible for Watsi funding, patients must be afflicted with a condition that, if left untreated, will severely impact their standard of living. Treatments eligible for Watsi funding cost less than $3000 USD, have a high probability of success, and are financially out-of-reach for the patient in need. In short, Watsi treatments are low-cost and high-impact.
              </p>
            </section>

            <section class="verse">
              <h3 class="verse__heading" id="about">Can Watsi patients pay for treatment?</h3>
              <p class="verse__copy">
              No. Watsi only posts profiles for patients who are unable to afford the medical care they need as a result of poverty. Our Medical Partners use their local knowledge and experience to vet the patients that apply for Watsi care. However, in most cases, our Medical Partners operate in communities that are so deprived economically that it is unrealistic to expect anyone living there to be able to afford the type of medical care Watsi donors fund.
              </p>
            </section>
            
            <section class="verse">
              <h3 class="verse__heading" id="about">Who decides which patients receive Watsi funding?</h3>
              <p class="verse__copy">
              When Medical Partners send us patient profiles, we confirm that they meet our criteria. We cannot see and evaluate patients ourselves, so we trust that our Medical Partners are choosing the best treatments available to the patients, in their particular setting. Then, we post the profiles as we are able. In the instance we have more profiles than we can fund at any one time, we are committed to posting the lowest-cost, highest-impact profiles first.
              </p>
            </section>
            <section class="verse">
              <h3 class="verse__heading" id="about">What if a profile isn't funded?</h3>
              <p class="verse__copy">
              Profiles are posted on Watsi until they are fully funded. We work closely with our Medical Partners to determine how many profiles we will be able to fund based on current donor demand. If donor demand increases, we increase the number of profiles our partners can submit for funding, and vice versa.
              </p>
            </section>
            <section class="verse">
              <h3 class="verse__heading" id="about">How do I know the treatment will be a success?</h3>
              <p class="verse__copy">
              Every treatment posted on Watsi is considered to have a high probability of success by the Medical Partner that submitted it. We trust their local knowledge and expertise to make that decision. That said, we can’t guarantee that every treatment funded by Watsi will be successful.
              </p>
            </section>
            <section class="verse">
              <h3 class="verse__heading" id="about">What if a patient passes away?</h3>
              <p class="verse__copy">
              Watsi Medical Partners are well-respected, trusted organizations that are committed to providing the highest level of care possible. In extreme, heartbreaking circumstances, it’s possible that a patient may not make it. If a patient passes away before receiving a treatment funded by Watsi, Watsi donors will be given the option to reallocate their donation to another patient or receive a full refund. If a patient passes away after receiving a treatment funded by Watsi, Watsi donors will receive an update notifying them about what happened.
              </p>
            </section>
            <section class="verse">
              <h3 class="verse__heading" id="about">What if a patient doesn't receive treatment?</h3>
              <p class="verse__copy">
              In rare cases, a patient may not receive the treatment that was funded for them on Watsi. This may happen if a patient decides against treatment at the last minute, no longer needs care, or passes away before treatment is provided. If this happens, Watsi will give donors the option to reallocate their donations to another Watsi patient, or receive a full refund.
              </p>
            </section>
            <section class="verse">
              <h3 class="verse__heading" id="about">What are treatments so cheap?</h3>
              <p class="verse__copy">
              Medical care in developing countries is significantly less expensive than in the U.S. and other developed nations. We also work closely with our Medical Partners to find creative ways to reduce the cost of medical care for Watsi patients. In many cases, a percentage of the treatment cost is subsidized by the local government and/or doctors agree to donate their time.  
              </p>
            </section>
            <section class="verse">
              <h3 class="verse__heading" id="about">Why doesn't Watsi operate in the United States?</h3>
              <p class="verse__copy">
              Our mission is to fund low-cost, high-impact medical care. While we’re not opposed to working in the United States, we currently focus our efforts in developing countries because it’s more frequently the case that a relatively small amount of money stands in the way of someone receiving life-changing care.
              </p>
            </section>
            <section class="verse">
              <h3 class="verse__heading" id="about">What if a patient needs medical care before their profile is funded?</h3>
              <p class="verse__copy">
              In some cases, Medical Partners may need to provide medical care to a patient before their profile is fully funded on Watsi. We allow this because we know that when a patient has a serious injury or illness, it sometimes requires immediate attention, and we are committed to doing whatever it takes to allow our Medical Partners to do their jobs as best they can. Once we receive an update from the Medical Partner and the profile has been fully funded on Watsi, we transfer the funds to the Medical Partner to cover the cost of care.
              </p>
            </section>
            <section class="verse">
              <h3 class="verse__heading" id="about">What is a Medical Partner?</h3>
              <p class="verse__copy">
              Medical Partners are on-the-ground organizations that provide reliable medical care to underserved populations in developing countries. In order for Watsi to form a relationship with a Medical Partner, they must maintain the highest of ethical standards and fit the following criteria:
                <ol class="verse__list--ordered">
                  <li class="verse__item">
                    Must be a registered legal and licensed entity in its country of operation.
                  </li>
                  <li class="verse__item">
                  Must have a history (at least 3 years) of providing reliable medical care.
                  </li>
                  <li class="verse__item">
                  Must have a strong positive reputation within the local and national medical community.
                  </li>
                  <li class="verse__item">
                  Must provide safe and ethical medical care and be well-respected by the people they serve.
                  </li>
                  <li class="verse__item">
                  Must agree to allow Watsi to perform due diligence, initial and ongoing, to ensure compliance with specified financial, legal and ethical regulations.
                  </li>
                  <li class="verse__item">
                  Must be willing and able to designate a staff member to upload patient profiles and provide updates to donors.
                  </li>
                  <li class="verse__item">
                  Must have a successful working relationship with at least one US-based 501(c)(3) non-profit organization.
                  </li>
                </ol>
              </p>
            </section>
            <section class="verse">
              <h3 class="verse__heading" id="about">How does an organization become a Medical Partner?</h3>
              <p class="verse__copy">
              If you know an organization that is interested in partnering with Watsi, please feel free to email us at <a class="verse__link" href=":mailto:connect@watsi.org">connect@watsi.org</a>. We are always looking for quality Medical Partners that align with our values.
              </p>
            </section>

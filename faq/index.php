          <div class="chapter__body">

            <section class="verse">
              <h3 class="verse__heading" id="about">What is Watsi?</h3>
              <p class="verse__copy">
              Watsi is a global crowdfunding platform for healthcare that enables anyone to donate as little as $5 to directly fund life-changing medical care for people in need. Watsi is a registered U.S. 501(c)(3) non-profit organization.
              </p>
            </section>

            <section class="verse">
              <h3 class="verse__heading" id="about">Why health care?</h3>
              <p class="verse__copy">
              We believe that every person deserves access to medical care, regardless of their nationality, religion, or socioeconomic status. We envision a world where people no longer die from conditions that society has the knowledge, resources, and power to treat. Watsi isn’t a charity. Watsi is a non-profit startup bringing the world together to provide access to a fundamental human right. 
              </p>
            </section>

            <section class="verse">
              <h3 class="verse__heading" id="about">What does Watsi mean?</h3>
              <p class="verse__copy">
              On December 3, 2010, Chase was traveling on a bus in the Talamanca Indigenous Territory of Costa Rica when a woman boarded the bus and asked the passengers for donations to pay for her son’s medical treatment. This experience led to the birth of Watsi, named after the town Chase was traveling through at the time. 
              </p>
            </section>
            <section class="verse">
              <h3 class="verse__heading" id="about">How does Watsi fund its day to day operations?</h3>
              <p class="verse__copy">
              100% of every donation made on Watsi directly funds medical treatment for the person you choose. We cover our overhead separately through private donors, foundations, and corporations. 
              </p>
            </section>
            <section class="verse">
              <h3 class="verse__heading" id="about">Can I see your financials?</h3>
              <p class="verse__copy">
              Of course! We are 100% transparent. Period. All of our financials are publicly available on our <a class="verse__link" href="https://docs.google.com/spreadsheet/pub?key=0Ah3wJ9CRQzyHdDZSaEF1X1JsRm1yZ251d0RQZ0VmRGc&gid=0" target="_blank">Google Transparency Document</a>. If you have any questions, please email <a class="verse__link" href="mailto:connect@watsi.org">connect@watsi.org</a> and we’ll be happy to help.
              </p>
            </section>


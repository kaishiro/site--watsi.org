        <div class="chapter">

          <h3 class="chapter__heading">Donations</h3>

          <div class="chapter__body">

            <section class="verse">
              <h3 class="verse__heading" id="about">Really? 100% of my donation goes to the chosen patient?</h3>
              <p class="verse__copy">
              Yes, 100% of your donation covers the cost of medical care for the patient of your choice. Your donation is not pooled into a general programming fund, your donation does not go to help run Watsi’s operations, and your donation does not go to fundraising activities. Watsi even covers the credit card processing fees (2.4% + $0.30 per transaction) so that every cent of your donation directly funds medical treatment.
              </p>
            </section>

            <section class="verse">
              <h3 class="verse__heading" id="about">How do I know my money will go to fund the treatment I choose?</h3>
              <p class="verse__copy">
              Being completely transparent about where your donation goes is one of our highest values. We ensure 100% transparency in a number of different ways:

                <ol class="verse__list--ordered">
                  <li class="verse__item">
                  Publication of all financial transactions: We post all of our financials on our Google Transparency Document, including screenshots of funds transfers for every patient.
                  </li>
                  <li class="verse__item">
                  Real updates: You will receive an update and photo about the outcome of the treatment you funded.
                  </li>
                  <li class="verse__item">
                  Partnering with the best: We work with organizations that have received distinction for their work and are well-respected in the global health community. To learn more about our Medical Partners, you can view their information on each patient profile on Watsi.
                  </li>
                  <li class="verse__item">
                  Double Due-Diligence: We have strong personal relationships with each of our Medical Partners (the Executive Director of our first Medical Partner flew to San Francisco and slept on Chase’s couch for a week to discuss the partnership), but we always perform initial and ongoing due diligence to ensure they meet our Medical Partner Criteria (see “What is a Medical Partner”). Because all of our Medical Partners are affiliated with a 501(c)(3) non-profit organization in the US, they are also subject to the tax-exempt rules and regulations of the Internal Revenue Service and are required to release all of their financial information for inspection on an annual basis. Therefore, all of our Medical Partners undergo due diligence investigations by both Watsi and the US Government to ensure that they are organizations of the highest integrity.
                  </li>
                  <li class="verse__item">
                  Direct transfers: Watsi funds are transferred directly to our Medical Partner for the purpose of covering the cost of the patient’s medical treatment. They are never handled by the patient and are never released to anyone for discretionary spending.
                  </li>
                </ol>
              </p>
            </section>

            <section class="verse">
              <h3 class="verse__heading" id="about">How do the donation logistics work?</h3>
              <p class="verse__copy">
              The Medical Partner identifies a patient that needs low-cost, high-impact medical care and submits the profile to Watsi. If the profile meets our criteria, we accept it and guarantee the Medical Partner that we will cover the cost of providing care to that patient. Watsi posts the profile online, Watsi donors fund it, and the Medical Partner provides care to the patient. After the treatment is provided, the Medical Partner sends Watsi an update about the outcome of the treatment. Once Watsi receives the update, we send it to the donors who supported that patient and transfer the funds to the Medical Partner’s PayPal account to cover the cost of the treatment.
              </p>
            </section>

            <section class="verse">
              <h3 class="verse__heading" id="about">Why are funds transferred to Medical Partners via PayPal?</h3>
              <p class="verse__copy">
              Watsi transfers funds to each Medical Partner’s verified PayPal account. It’s up to the Medical Partner to withdraw and/or transfer those funds however is most cost effective for their organization. The majority of our Medical Partners withdraw Watsi funds to a US bank account and bundle international transfers periodically in order to reduce the cost of international transfer fees.
              </p>
            </section>

            <section class="verse">
              <h3 class="verse__heading" id="about">Why doesn't Watsi transfer funds before medical care is provided?</h3>
              <p class="verse__copy">
              We did this in the beginning, but after conversations with our Medical Partners and counseling from trusted advisers, we decided that it actually makes more sense to transfer funds immediately after care is provided. All patient profiles are accepted by Watsi before the patient receives care. This enables Medical Partners to treat patients whenever makes the most medical sense, operating under the guarantee that Watsi will cover the cost of care. For a detailed description about how we arrived at this conclusion, see this blog post.
              </p>
            </section>

            <section class="verse">
              <h3 class="verse__heading" id="about">Am I really funding medical care for the patient?</h3>
              <p class="verse__copy">
              Yes. Your donation directly covers the cost of care for the patient you choose. Medical Partners provide care to patients accepted by Watsi operating under the guarantee that the cost of care will be paid for by donors. In this sense, Watsi operates much like a traditional health insurance company. When you go to the doctor, your doctor calls your health insurance provider to ensure they’ll cover the cost of care. Your doctor then provides care operating under the guarantee that it will be paid for by the insurance company.
              </p>
            </section>

            <section class="verse">
              <h3 class="verse__heading" id="about">Would patients still receive care if their treatment wasn’t funded?</h3>
              <p class="verse__copy">
              This is a tough one. What we’re finding is that it depends on the Medical Partner and the case. In some cases, there is no way the patient would receive care without Watsi. In other cases, it’s entirely possible that the Medical Partner would find another way to pay for the patient’s care, perhaps through another donor or organization. What we do know is that each case funded on Watsi represents one of two things: an expansion of the capacity of the Medical Partner to reach someone they couldn’t serve before, or a push to ensure that the Medical Partner is able to serve that one additional person.
              </p>
            </section>

            <section class="verse">
              <h3 class="verse__heading" id="about">Do I have to use PayPal to fund a treatment?</h3>
              <p class="verse__copy">
              You can use any major credit card or your PayPal account to fund a treatment. Either way, PayPal will process your payment so that your financial information is secure.
              </p>
            </section>

            <section class="verse">
              <h3 class="verse__heading" id="about">How do I know my financial information is protected?</h3>
              <p class="verse__copy">
              Your safety and privacy are important to us. We use PayPal, an internationally trusted financial services company, to process all transactions in order to ensure that your information remains secure. Read more about PayPal’s privacy policies here.
              </p>
            </section>

            <section class="verse">
              <h3 class="verse__heading" id="about">Is my donation tax deductible?</h3>
              <p class="verse__copy">
              Yes. All donations are tax deductible in the U.S. After making a donation on Watsi, you will receive an official tax-deductible receipt at the email you provided.
              </p>
            </section>

            <section class="verse">
              <h3 class="verse__heading" id="about">Do you have a question that wasn't answered here?</h3>
              <p class="verse__copy">
              If you have a question that wasn’t answered here, please email your question to connect@watsi.org and we will respond to you as soon as we are able. If we receive multiple questions about a similar topic, we will add that topic to this FAQ so that it is easily accessed by other people.
              </p>
            </section>

